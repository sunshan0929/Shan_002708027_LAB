package Genre;

/**
 *
 * @author shaoyifan
 */
public class Genre {
    
    String name;
    String id;

    public Genre() {
    }

    public Genre(String name, String id) {
        this.name = name;
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    @Override
    public String toString() {
        return  this.name;
    }
    
    
    
    
    
}
