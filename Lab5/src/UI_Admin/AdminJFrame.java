    private void RegisterBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RegisterBtnActionPerformed
        // TODO add your handling code here:
        if(FieldRole.getText().equals("customer")){
            this.business.getCustomerDirectory().createCustomer(FieldUserName.getText(), FieldPassword.getText(), FieldRole.getText(), FieldID.getText(), FieldName.getText(), FieldAge.getText());
            populate();
        }
        else if(FieldRole.getText().equals("librarian")){
            this.business.getLibrarianDirectory().createLibrarian(FieldUserName.getText(), FieldPassword.getText(), FieldRole.getText(), FieldID.getText(), FieldName.getText(), FieldAge.getText());
            populate();
        }
        else if(FieldRole.getText().equals("sysadmin")){
            this.business.getAccountDirectory().createUserAccount(FieldUserName.getText(), FieldPassword.getText(), FieldRole.getText());
            populate();
        }
        else
             JOptionPane.showMessageDialog(null, "invalid role entered!");
            
        
    }//GEN-LAST:event_RegisterBtnActionPerformed

