    public void populate(){
        UserAccountDirectory useraccountDirectory = this.business.getAccountDirectory();
        
        tableModel.setRowCount(0);
        for (UserAccount u : this.business.getAccountDirectory().getUserAccountList()) {
            
            Object[] row = new Object[7];
            
            row[0] = u.getAccountId();
            row[1] = u.getUsername();
            row[2] = u.getPassword();
            row[3] = u.getRole();
            if(u.getRole().equals("customer")){
                row[4]=this.business.getCustomerDirectory().findByAccountId(u.getAccountId()).getPersonID();
                 row[5]=this.business.getCustomerDirectory().findByAccountId(u.getAccountId()).getName();
                  row[6]=this.business.getCustomerDirectory().findByAccountId(u.getAccountId()).getAge();
            }
            else if(u.getRole().equals("librarian")){
               row[4]=this.business.getLibrarianDirectory().findByAccountId(u.getAccountId()).getPersonID();
                     row[5]=this.business.getLibrarianDirectory().findByAccountId(u.getAccountId()).getName();
                  row[6]=this.business.getLibrarianDirectory().findByAccountId(u.getAccountId()).getAge();
            }
            
            tableModel.addRow(row);
        }
    
    
    }
    
